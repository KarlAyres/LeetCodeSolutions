    public static int[] kWeakestRows(int[][] mat, int k) {
        int totalNumberOfRows = mat.length;
        int numberOfWeakestRows = k;
        int[] sumOfSoldiersArray = new int[totalNumberOfRows];
        int[] orderedSoldiersArray;


        // Iterate over each row and create array sum of soldiers
        System.out.println("The number of soldiers in each row is:");
        for (int i = 0; i < totalNumberOfRows; i++) {
            for (int j = 0; j < mat[i].length; j++) {
                sumOfSoldiersArray[i] += mat[i][j];
            }
            System.out.println(" - Row " + i + ": " + sumOfSoldiersArray[i]);
        }

        // Clone and sort sum of soldiers array
        orderedSoldiersArray = sumOfSoldiersArray.clone();
        Arrays.sort(orderedSoldiersArray);

        // Compare sorted array to sum array and replace with index position of sorted array
        for (int i = 0; i < orderedSoldiersArray.length; i++) {
            for (int j = 0; j < sumOfSoldiersArray.length; j++) {
                if (orderedSoldiersArray[i] == sumOfSoldiersArray[j]) {
                    orderedSoldiersArray[i] = j;
                    sumOfSoldiersArray[j] = -1;
                    break;
                }
            }
        }
        System.out.println("The rows ordered from weakest to strongest are " + Arrays.toString(orderedSoldiersArray));
        return Arrays.copyOfRange(orderedSoldiersArray, 0, k);
    }
